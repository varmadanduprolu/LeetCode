package com.varma.leetcode;

import java.util.Scanner;

public class Permutations_46 {
        public static void main(String[] args) {
            Scanner sc=new Scanner(System.in);
            int len=sc.nextInt();
            int nums[]=new int[len];
            for (int i = 0; i < nums.length; i++) {
                nums[i]=sc.nextInt();
            }
        }
    }

